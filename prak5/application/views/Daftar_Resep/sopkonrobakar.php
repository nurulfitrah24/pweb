    <h2>Sop Konro Bakar</h2>
     <p>Porsi untuk 3 sampai 4 orang</p>
   <h3>Bahan-bahan:</h3>
   <ol>
    <li>50 kg iga sapi yg dagingnya masih ada ya</li>
    <li>6 siung baput</li>
    <li>2 ruas lengkuas</li>
    <li>Lada putih</li>
    <li>Ketumbar</li>
    <li>Garam</li>
    <li>Gula</li>
    <li>Kaldu bubuk</li>
    <li>Kluwak</li>
    <li>2 batang sereh</li>
    <li>Dayn salam</li>
    <li>Daun bawang</li>
    <li>Bumbu kacang</li>
    <li>Bumbu pecel instan</li>
    <li>Saos kacang crunchy</li>
    <li>Kecap</li>
   </ol>

 <h3>Langkah</h3>
  <ol>
    <li>Bersihkan iga, rebus sampai kaldunya keluar
    <li>Haluskan baput, bamer, lengkuas, dan kluwak. Kluwak sebelumnya direndam dulu dengan air panas
    <li>Tumis bumbu halus. Tambahkan merica dan ketumbar
    <li>Masukkan bumbu halus ke dalam kuah, tunggu sampai mendidih. Tambahkan serreh dan daun salam. Tambahkan garam, gula, dan kaldu bubuk sesuai selera
    <li>Jika sudah mendidih, tiriskan iga. Bakar di grill dengan api sedang
    <li>Untuk saos kacang, campurkan bumbu pecel instan dengan saos kacang crunchy. Supaya cepat campurnya pake air panas. Tambahkan kecap
    <li>Konro bakar siap dinikmati dengan daun bawang, bawang goreng, jeruk nipis, dan sambel. Selamat menikmati :))
  </ol>

