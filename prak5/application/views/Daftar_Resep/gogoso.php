    <h2>Gogoso</h2>

    <h3>Bahan- bahan :</h3>
     <ol>
       <li>250 gram beras ketan</li>
       <li>3 lembar daun salam</li>
       <li>1 batang serai memarkan</li>
       <li>1/2 sdt garam</li>
      </ol>

    <h3>Isi</h3>
     <ol>
      <li>Abon Sapi atau daging ayam, sesuai selera</li>
     </ol>

     <h3>Pembungkus</h3>
      <ol>
       <li>daun pisang muda secukupnya(untuk dalaman)</li>
       <li>daun pisang setengah tua (untuk luaran)</li>
       <li>tusuk gigi</li>
      </ol>

      <h3>Cara Membuat</h3>
       <ol>
        <li>Ketan rendam selama 2 jam, cuci bersih, tiriskan dan kukus selama 15 menit, angkat.</li>
        <li>Rebus santan bersama daun salam, serai dan garam, masukkan ketan yang telah dikukus dan aroni hingga seluruh santan dihisap oleh ketan, biarkan hingga setengah matang, angkat.</li>
        <li>bungkus adonan ketan dengan daun pisang muda jangan lupa masukkan isian abonya. kemudaian isi lagi dengan daun pisang yang agak tua bungkus menyerupai permen. dan ujungnya tusuk dengan tusuk gigi.</li>
