    <h2>Pallu Basa</h2>
     <p>Pallu basa adalah makanan tradisional Makassar, Sulawesi Selatan.
       Seperti Coto Mangkasara (Coto Makassar), Pallubasa juga terbuat dari Daging sapi,
       jeroan (isi dalam perut) sapi atau kerbau . Namun kini kuliner ini telah
        berkembang menjadi pallu basa ayam dan pallu basa kepala ikan. Proses
        memasaknya pun hampir sama dengan Coto Makassar yang lebih terkenal,
        yakni jeroan direbus dalam waktu lama. Setelah matang, jeroan ditambah
        dengan daging itu diiris-iris, kemudian ditaruh/dihidangkan dalam mangkuk
        yang lebih enak jika disajikan panas atau hangat.</p>
    <h3>Bahan-bahan:</h3>
    <ol>
      <li>Daging sapi - 250 gram</li>
      <li>Kaldu sapi - 600 ml</li>
      <li>Kelapa parut, sangrai lalu haluskan hingga berminyak - 30 gram</li>
      <li>Serai, memarkan - 1 batang</li>
      <li>Lengkuas, memarkan - 2 cm</li>
      <li>Gula merah - 1 sdt</li>
      <li>Kayu manis - 1 cm</li>
      <li>Cengkeh - 4 buah</li>
      <li>Air asam - 30 ml</li>
      <li>Garam - 1 sdt</li>
      <li>Penyedap rasa (opsional) - 1 sdt</li>
      <li>Minyak, untuk menumis - 3 sdm</li>
    </ol>
    <h3>Bumbu Halus:</h3>
  <ol>
    <li>Bawang putih - 5 siung</li>
    <li>Bawang merah - 8 butir</li>
    <li>Cabai keriting - 5 buah</li>
    <li>Jahe - 2 cm</li>
    <li>Pala - 1/2 biji</li>
    <li>Ketumbar - 1 sdt</li>
    <li>Jinten - 1/2 sdt</li>
  </ol>

