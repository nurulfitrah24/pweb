    <h2>Kue Barongko Pisang</h2>
    <p>Untuk 7 Bungkus</p>

    <h3>Bahan- bahan :</h3>
     <ol>
       <li>10 buah pisang kepok matang</li>
       <li>1/2 sendok teh garam</li>
       <li>100 ml santan dari 1/4 butir kelapa</li>
       <li>daun pisang untuk membungkus</li>
     </ol>

     <h3>Cara membuat :</h3>
      <ol>
        <li>Kupas pisang, ambil bagian tengahnya. Haluskan.</li>
        <li>Campur pisang, gula pasir, garam, telur, dan santan. Aduk rata.</li>
        <li>Ambil daun pisang. Sendokkan campuran pisang. Bungkus tum. Semat dengan lidi.</li>
        <li>Kukus 25 menit diatas api sedang sampai matang.</li>
      </ol>

