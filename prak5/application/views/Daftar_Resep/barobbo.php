    <h2>Barobbo</h2>
     <p>Barobbo adalah salah satu makanan khas makassar atau biasa kita sebut juga bubur jagung. Bisa untuk 8 sampai 10 orang.</p>
  <h3>Bahan- bahan :</h3>
   <ol>
    <li>8 jagung kuning, serut halus</li>
    <li>1 cup beras</li>
    <li>2 ikat kangkung, siangi daun dan batangnya</li>
    <li>1 ikat bayam merah, siangi daun dan batangnya</li>
    <li>300 gr labu kuning, potong kotak</li>
    <li>Daun kemangi</li>
    <li>300 gr udang, buang kepala dan kulitnya</li>
    <li>1 batang besar daun bawang, iris tipis</li>
    <li>5 liter air</li>
    <li>Garam</li>
    <li>Ikan asin goreng</li>
    <li>Sambal</li>
    <li>1 buah tomat ukuran sedang</li>
    <li>15 cabe rawit</li>
    <li>1 sdt garam</li>
  </ol>

 <h3>Langkah</h3>
  <ol>
    <li>Masukkan sekitar 1-2 liter air ke dalam panci, masukkan beras. Masak hingga mengembang</li>
    <li>Masukkan sisa air dan jagung. Masak hingga setengah matang</li>
    <li>Masukkan labu, masak hingga labu menjadi lunak. Jangan lupa diaduk hingga ke dasar agar tidak hangus di bagian bawah.</li>
    <li>Masukkan kangkung, daun bawang, bayam dan kemangi. Masak hingga layu. Jangan lupa tetap diaduk.</li>
    <li>Masukkan garam, cek rasa. Matikan kompor.</li>
    <li>Ulek semua bahan sambal hingga halus.</li>
    <li>Sajikan barobbo dengan ikan asin goreng, jeruk nipis dan sambal.</li>
   </ol>

