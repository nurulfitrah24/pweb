    <h2>Jalangkote</h2>

    <h3>Bahan- bahan :</h3>
     <ol>
       <li>500 gram tepung terigu protein sedang</li>
       <li>1 sdt garam</li>
       <li>2 butir telur ayam</li>
       <li>2 sdm Margarine</li>
       <li>750 ml minyak goreng</li>
     </ol>

  <h3>Isi Jalangkote</h3>
      <ol>
        <li>2 buah kentang, potong dadu</li>
        <li>2 buah wortel, potong dadu</li>
        <li>200 gram daging sapi cincang atau daging ayam</li>
        <li>150 gram Mihun kering</li>
        <li>5 butir telur rebus, potong menjadi 6 bagian</li>
        <li>100 gram touge, siangi</li>
        <li>25 gram saoun, rendam hingga lunak</li>
        <li>Minyak goreng</li>
        <li>25 ml air</li>
        <li>Merica secukupnya</li>
       </ol>

  <h3>Bumbu Jalangkote</h3>
       <ol>
         <li>1/2 sdt merica bubuk</li>
         <li>1/4 sdt pala bubuk</li>
         <li>3 siung bawang putih, iris tipis-tipis</li>
         <li>4 butir bawang merah, iris tipis</li>
         <li>Garam secukupnya</li>
         <li>Gula pasir secukupnya</li>
         <li>Bumbu kaldu bubuk secukupnya</li>
       </ol>

   <h3>Saos Jalangkote</h3>
       <ol>
         <li>5 siung bawang putih</li>
         <li>4 butir bawang merah</li>
         <li>5 buah cabai merah</li>
         <li>3 buah cabai rawit (sesuai selera)</li>
         <li>1/2 sdt cuka</li>
         <li>1 sdt gula pasir</li>
         <li>200 ml air</li>
         <li>Garam secukupnya</li>
       </ol>

   <h3>Cara Membuat Jalangkote</h3>
       <ol>
         <li>Menyiapkan isI jalangkote, panaskan 3 sdm minyak goreng. Tumis bawang merah dan bawang putih sampai harum. Masukkan bawang perai, daging cincang, wortel dan kentang. Tuangkan sedikit air. Masak hingga sedikit matang. Masukkan bahan isi serta bumbu lainnya kecuali irisan telur rebus. Lanjutkan memasak bahan hingga matang. Angkat lalu sisihkan.</li>
         <li>Cara membuat kulitnya. Campurkan tepung terigu dalam air, tambahkan 2 sendok margarine, telur, aduk hingga rata. Tambahkan sedikit demi sedikit air garam sambil diaduk dan uleni adonan. Perkirakan jumlah air tidak terlalu banyak, agar adonan tidak terlalu lengket dan lembek, jika terlalu banyak air dapat ditambahkan tepung lagi. Jika adonan sudah pas, gilas adonan dengan ketebalan 3 mm, buat pola lingkaran dengan diameter 10 cm.</li>
         <li>Mengisi kulit adonan isi Jalangkoteâ€™. Ambil satu lembar kulit yang sudah digilas, taruh 2 sendok makan adonan isi dan sepotong telur diatasnya. Katupkan kedua sisi kulit dan rekatkan dengan putih telur sehingga membentuk setengah lingkaran. Pilin tepinya, dari ujung ke ujung hingga tidak ada lagi celah, dan semua isi tertutupi.</li>
         <li>Panaskan minyak goreng, goreng adonan yang sudah dipilin hingga berwarna coklat. Angkat dan tiriskan.</li>
         <li>Membuat saus, campur semua bahan saus kecuali air dan cuka dengan diblender. Tumis bahan saus, tambahkan air secukupnya. Tambahkan cuka. Tumis hingga matang.</li>
       </ol>

