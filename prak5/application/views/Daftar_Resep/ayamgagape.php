    <h2>Ayam Gagape</h2>

    <h3>Bahan- bahan :</h3>
     <ol>
      <li>1 ekor ayam kampung, potong menjadi delapan bagian</li>
      <li>1 sdm bawang goreng</li>
      <li>500 ml santan dari 1 butir kelapa</li>
    </ol>

    <h3>Haluskan</h3>
      <ol>
       <li>4 siung bawang putih</li>
       <li>8 butir bawang emrah</li>
      </ol>

    <h3>Bumbu Lainnya:</h3>
      <ol>
        <li>1 sdt merica</li>
        <li>1 butir jeruk nipis, ambil airnya</li>
        <li>2 batang serai, memarkan</li>
        <li>1 cm lengkuas, memarkan</li>
        <li>3 lembar daun salam</li>
        <li>1/2 buitr kelapa, parut, sangrai, tumbuk</li>
        <li>1 sdm garam</li>
      </ol>

    <h3>Cara Buat :</h3>
      <ol>
        <li>Lumuri ayam dengan air jeruk nipis dan bumbu yang sudah dihaluskan, biarkan selama 15 menit.</li>
        <li>Rebus santan, serai, lengkuas, daun salam, dan garam dengan api sedang hingga santan mendidih. Aduk sesekali agar santan tidak pecah.</li>
        <li>Masukkan ayam yang sudah dilumuri bumbu dan kelapa sangrai, aduk terus hingga ayam empuk. Angkat</li>
        <li>Sajikan dengan taburan bawang goreng.</li>
     </ol>

