<h2>Kambu Paria khas Makassar</h2>

<h3>Bahan- bahan :</h3>
 <ol>
  <li>600 gram ikan tongkol</li>
  <li>Minyak goreng untuk menumis</li>
  <li>2 kuning telur, kocok</li>
  <li>750 gram buah paria</li>
  <li>2 putih telur, untuk olesan</li>
  <li>5 buah cabe merah, iris serong</li>
  <li>5 buah cabe hijau, iris serong</li>
  <li>800 ml santan encer</li>
  <li>200 ml santan kental, dari 1 butir kelapa</li>
  <li>10 buah belimbing sayur, belah 2</li>
 </<ol>

<h3>Bumbu Halus 1 :</h3>
 <ol>
  <li>5 siung bawang putih</li>
  <li>5 butir bawang merah</li>
  <li>1 sdt ketumbar</li>
  <li>1 sdt merica</li>
  <li>1 sdt garam</li>
 </ol>

<h3>Bumbu Halus 2 :</h3>
 <ol>
  <li>10 buah bawang merah</li>
  <li>6 siung bawang putih</li>
  <li>2 cm lengkuas</li>
  <li>4 batang serai, iris</li>
  <li>1 sdt garam</li>
 </ol>

<h3>Cara Masak :</h3>
 <ol>
  <li>Kukus ikan, ambil dagingnya, lumatkan, sisihkan. Sangrai kelapa hingga kuning dan kering. Angkat,sisihkan. Panaskan 1 sdm minyak goreng, tumis bumbu halus I hingga harum, angkat. Campur dengan kelapa sangrai, gerus hingga keluar minyaknya. Campur dengan ikan lumat, masukkan kuning telur. Aduk hingga rata, sisihkan.</li>
  <li>Potong buah paria melintang, tebal 3-4 cm. Rebus hingga setengah masak, angkat. Buang bijinya hingga paria berlubang di tengah. Olesi bagian dalamnya dengan putih telur. Isi dengan adonan ikan hingga penuh dan padat. Sisakan Â± 2 sdm adonan ikan.</li>
  <li>Panaskan 2 sdm minyak, tumis bumbu halus II hingga harum. Masukkan cabe iris dan santan encer, didihkan. Masukkan paria isi, masak hingga matang, masukkan sisa adonan ikan, belimbing sayur, santan kental, timba-timba santan. Setelah mendidih, angkat.</li>
  <li>Hidangkan</li>
 </ol>

